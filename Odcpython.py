menu = {
    "1": {"item": "Phane", "price": 50.50},
    "2": {"item": "Logala", "price": 80.95},
    "3": {"item": "Lerotse", "price": 70.50},
    "4": {"item": "Tshotlho", "price": 40.95},
    "5": {"item": "Moro", "price": 10.50},
    "6": {"item": "Exit", "price": 0.0} # To exit the menu
  }  
# Initialize an empty list to store the selected items
selected_items = []
# Display the menu to the user
print("Re a lo amogela mo Kwena sereto, rena le dijo tse di latelang:")
for key, value in menu.items():
    print(f"{key}. {value['item']}: P{value['price']}")

# Ask the user to select items until they choose to exit
while True:
    choice = input("Kwala nomoro ya dijo tse lo di batlang (kana '6' go tlogela): ")
    
    if choice == "6":
        break
    
    if choice in menu:
        selected_items.append(menu[choice])

# Calculate the total amount
total_amount = sum(item["price"] for item in selected_items)

# Display the selected items and total amount
if selected_items:
    print("\nSelected Items:")
    for item in selected_items:
        print(f"{item['item']}: P{item['price']}")
    print(f"Total Amount: P{total_amount:.2f}")
else:
    print("Re solofela fa o tla nna le letsatsi le le molemo.Sala sentle!")
